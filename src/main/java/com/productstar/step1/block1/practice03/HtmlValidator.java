
package com.productstar.step1.block1.practice03;

// Импортируем Stack - коллекцию для хранения элементов в порядке LIFO (последним пришёл - первым вышел)
// Stack работает как стопка тарелок: кладёшь сверху, берёшь тоже сверху
import java.util.Stack;

// Объявляем класс HtmlValidator (проверяет правильность HTML-разметки)
public class HtmlValidator {

    // ========== ОСНОВНОЙ МЕТОД ДЛЯ ПРОВЕРКИ HTML ==========
    // Метод принимает строку html и возвращает boolean (true - правильно, false - неправильно)
    public static boolean isValidHtml(String html) {

        // СОЗДАЁМ СТЕК ДЛЯ ХРАНЕНИЯ ОТКРЫВАЮЩИХ ТЕГОВ
        // Stack<String> - стек, который хранит строки (названия тегов)
        Stack<String> stack = new Stack<>();

        // ПРОХОДИМ ПО ВСЕМ СИМВОЛАМ СТРОКИ
        // i = 0 - начинаем с первого символа
        // i < html.length() - пока не дошли до конца строки
        // i++ - переходим к следующему символу
        for (int i = 0; i < html.length(); i++) {

            // ПОЛУЧАЕМ ТЕКУЩИЙ СИМВОЛ (один символ строки)
            char c = html.charAt(i);

            // ========== ЕСЛИ НАШЛИ ОТКРЫВАЮЩУЮ СКОБКУ '<' ==========
            // Начинается новый тег
            if (c == '<') {

                // ПРОВЕРЯЕМ, НЕ ЗАКРЫВАЮЩИЙ ЛИ ЭТО ТЕГ?
                // Смотрим следующий символ (i+1)
                // Если следующий символ '/' - значит это закрывающий тег
                if (i + 1 < html.length() && html.charAt(i + 1) == '/') {

                    // ========== ЭТО ЗАКРЫВАЮЩИЙ ТЕГ (например </div>) ==========
                    // Ищем конец тега (символ '>')
                    // Начинаем со следующего символа после '/'
                    int j = i + 2;  // j - позиция после '/'

                    // Двигаемся вперёд, пока не найдём '>'
                    // j < html.length() - не выходим за границы строки
                    // html.charAt(j) != '>' - пока не встретили закрывающую скобку
                    while (j < html.length() && html.charAt(j) != '>') {
                        j++;
                    }

                    // ИЗВЛЕКАЕМ НАЗВАНИЕ ТЕГА (без < и / и >)
                    // substring(i+2, j) - берём часть строки от позиции i+2 до j
                    // Например: < / d i v > -> берём "div"
                    String tag = html.substring(i + 2, j);

                    // ПРОВЕРЯЕМ, ЕСТЬ ЛИ В СТЕКЕ ОТКРЫВАЮЩИЙ ТЕГ
                    // stack.isEmpty() - стек пуст? (значит не было открывающего тега)
                    if (stack.isEmpty()) {
                        // Если пуст - ошибка: закрывающий тег без открывающего
                        System.out.println("Ошибка: закрывающий тег </" + tag + "> без открывающего");
                        return false;  // возвращаем false - строка неправильная
                    }

                    // ДОСТАЁМ ВЕРХНИЙ ТЕГ ИЗ СТЕКА
                    // pop() - достаёт и УДАЛЯЕТ верхний элемент из стека
                    String openTag = stack.pop();

                    // ПРОВЕРЯЕМ, СОВПАДАЮТ ЛИ ТЕГИ
                    // Если открывающий тег не совпадает с закрывающим - ошибка
                    if (!openTag.equals(tag)) {
                        System.out.println("Ошибка: тег <" + openTag + "> закрыт тегом </" + tag + ">");
                        return false;  // возвращаем false - строка неправильная
                    }

                    // Перемещаем курсор i на позицию закрывающей скобки
                    // i = j - пропускаем символ '>'
                    i = j;

                } else {
                    // ========== ЭТО ОТКРЫВАЮЩИЙ ТЕГ (например <div>) ==========
                    // Ищем конец тега (символ '>')
                    int j = i + 1;  // j - позиция следующего символа после '<'

                    // Двигаемся вперёд, пока не найдём '>'
                    while (j < html.length() && html.charAt(j) != '>') {
                        j++;
                    }

                    // ИЗВЛЕКАЕМ НАЗВАНИЕ ТЕГА (без < и >)
                    // substring(i+1, j) - берём часть строки от позиции i+1 до j
                    // Например: < d i v > -> берём "div"
                    String tag = html.substring(i + 1, j);

                    // КЛАДЁМ НАЗВАНИЕ ТЕГА В СТЕК (наверх)
                    // push(tag) - добавляет элемент на вершину стека
                    stack.push(tag);

                    // Перемещаем курсор i на позицию закрывающей скобки
                    i = j;
                }
            }
        }

        // ========== ПРОВЕРЯЕМ, НЕ ОСТАЛОСЬ ЛИ НЕЗАКРЫТЫХ ТЕГОВ ==========
        // stack.isEmpty() - true если стек пуст (все теги закрыты)
        // !stack.isEmpty() - true если в стеке ещё есть элементы (незакрытые теги)
        if (!stack.isEmpty()) {
            // Если в стеке остались теги - значит они не были закрыты
            System.out.println("Ошибка: не закрыт тег <" + stack.pop() + ">");
            return false;  // возвращаем false - строка неправильная
        }

        // ВСЕ ПРОВЕРКИ ПРОЙДЕНЫ - СТРОКА КОРРЕКТНА
        System.out.println("HTML-разметка корректна");
        return true;  // возвращаем true - строка правильная
    }

    // ========== ГЛАВНЫЙ МЕТОД ДЛЯ ПРОВЕРКИ ==========
    // public static void main - точка входа в программу
    public static void main(String[] args) {

        // ТЕСТ 1: ПРАВИЛЬНАЯ РАЗМЕТКА
        // Теги вложены правильно
        String html1 = "<div><p>Привет</p></div>";
        System.out.println("Проверка: " + html1);
        isValidHtml(html1);

        System.out.println();  // пустая строка для разделения

        // ТЕСТ 2: НЕПРАВИЛЬНАЯ РАЗМЕТКА (теги закрыты в другом порядке)
        // <p> открыт, потом <div>, а закрывается сначала </p> (хотя должен </div>)
        String html2 = "<div><p>Привет</div></p>";
        System.out.println("Проверка: " + html2);
        isValidHtml(html2);

        System.out.println();

        // ТЕСТ 3: НЕПРАВИЛЬНАЯ РАЗМЕТКА (тег не закрыт)
        String html3 = "<div><p>Привет</div>";
        System.out.println("Проверка: " + html3);
        isValidHtml(html3);
    }
}
