package com.productstar.step1.block1.lesson4;

public class Main {
    // Статическая переменная - массив целых чисел, доступный для всех методов класса
    // Заранее заданный фиксированный массив
    static int[] data = {4, 3, 1, 5};

    // Главный метод - точка входа в программу
    public static void main(String[] args) {
        // Вызываем метод print() для вывода исходного массива
        // Должно вывести: [4, 3, 1, 5]
        print();

        // Вызываем метод sort() для сортировки массива
        sort();

        // Вызываем метод print() для вывода отсортированного массива
        // Должно вывести: [1, 3, 4, 5]
        print();
    }

    // Метод для сортировки массива в порядке возрастания
    // Используется алгоритм пузырьковой сортировки (bubble sort)
    static void sort() {
        // Получаем длину массива и сохраняем в переменную n
        // data.length - свойство, возвращающее количество элементов в массиве
        int n = data.length;

        // Внешний цикл: отвечает за количество проходов по массиву
        // Нам нужно сделать n-1 проходов, так как после каждого прохода
        // самый большой элемент "всплывает" в конец массива
        for (int i = 0; i < n - 1; i++) {

            // Внутренний цикл: сравнивает соседние элементы
            // n - i - 1: с каждым проходом уменьшаем количество сравнений,
            // так как последние i элементов уже отсортированы и находятся на своих местах
            for (int j = 0; j < n - i - 1; j++) {

                // Проверяем, нужно ли менять элементы местами
                // Если текущий элемент больше следующего, то порядок нарушен
                if (data[j] > data[j + 1]) {

                    // Меняем местами элементы data[j] и data[j+1]
                    // Для этого используем временную переменную temp

                    // Шаг 1: Сохраняем значение текущего элемента во временную переменную
                    int temp = data[j];

                    // Шаг 2: Присваиваем текущему элементу значение следующего элемента
                    data[j] = data[j + 1];

                    // Шаг 3: Присваиваем следующему элементу сохраненное значение из temp
                    data[j + 1] = temp;

                    // После этих трех шагов элементы поменялись местами
                }
            }
        }
    }

    // Метод для вывода содержимого массива в консоль
    static void print() {
        // Выводим открывающую квадратную скобку
        // System.out.print() - вывод без перехода на новую строку
        System.out.print("[");

        // Цикл для перебора всех элементов массива
        // i = 0 - начинаем с первого элемента (индекс 0)
        // i < data.length - продолжаем, пока индекс меньше длины массива
        // i++ - после каждой итерации увеличиваем индекс на 1
        for (int i = 0; i < data.length; i++) {
            // Выводим пробел и текущий элемент массива
            // Пробел добавляется для красивого форматирования между элементами
            System.out.print(" " + data[i]);
        }

        // После завершения цикла выводим закрывающую квадратную скобку
        // и переходим на новую строку с помощью println
        System.out.println(" ]");
    }
}