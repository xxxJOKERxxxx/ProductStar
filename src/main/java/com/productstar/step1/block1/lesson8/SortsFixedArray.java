// Объявляем пакет (путь к файлу в проекте)
package com.productstar.step1.block1.lesson8;

// Объявляем класс SortsFixedArray
public class SortsFixedArray {

    // Статический массив данных (доступен без создания объекта)
    // {4, 3, 1, 5} - исходные числа для сортировки
    static int[] data = {4, 3, 1, 5};

    // Главный метод - точка входа в программу
    public static void main(String[] args) {
        print();   // вызываем метод print() - печатаем массив ДО сортировки
        sort();    // вызываем метод sort() - сортируем массив
        print();   // вызываем метод print() - печатаем массив ПОСЛЕ сортировки
    }

    // Метод для сортировки массива (пузырьковая сортировка)
    static void sort() {
        // n = длина массива = 4
        int n = data.length;

        // ВНЕШНИЙ ЦИКЛ: отвечает за количество проходов
        // Нужно сделать (n-1) проходов, чтобы отсортировать все элементы
        // i = 0, 1, 2 (всего 3 прохода для массива из 4 элементов)
        for (int i = 0; i < n-1; i++) {

            // ВНУТРЕННИЙ ЦИКЛ: попарное сравнение и обмен
            // n-i-1: с каждым проходом нужно сравнивать на 1 элемент меньше
            // потому что самый большой уже "всплыл" в конец
            for (int j = 0; j < n-i-1; j++) {

                // Если текущий элемент БОЛЬШЕ следующего
                if (data[j] > data[j+1]) {

                    // МЕНЯЕМ элементы местами
                    // Сохраняем текущий элемент во временную переменную
                    int temp = data[j];
                    // На место текущего ставим следующий
                    data[j] = data[j+1];
                    // На место следующего ставим сохранённый текущий
                    data[j+1] = temp;
                }
            }
        }
    }

    // Метод для печати массива в консоль
    static void print() {
        System.out.print("[");  // печатаем открывающую квадратную скобку

        // Проходим по всем элементам массива
        for (int i = 0; i < data.length; i++) {
            System.out.print(" " + data[i]);  // печатаем пробел и значение элемента
        }

        System.out.print("]");  // печатаем закрывающую квадратную скобку
        System.out.println();   // переходим на новую строку
    }
}
