package com.productstar.step1.block1.StudentAndDocentGeneral;

 class Main {

    // 🔹 МЕТОД КЛАССА (static): можно вызывать без создания объекта Main
    static void greet(Person person) {
        // person — это ПАРАМЕТР МЕТОДА (временно хранит ссылку на объект)
        // Когда мы вызовем greet(student1), person будет "смотреть" на student1
        person.introduce();  // вызовется правильная версия: студента или доцента
    }

    // 🔹 ТОЧКА ВХОДА: с этого метода программа НАЧИНАЕТ выполняться
    public static void main(String[] args) {
        // 🔸 ЛОКАЛЬНЫЕ ПЕРЕМЕННЫЕ МЕТОДА main:
        // Они существуют ТОЛЬКО внутри main(), только пока программа работает

        // Шаг 1: создаём объект студента в памяти
        // new Student(...) → выделяет память, вызывает конструктор, возвращает ссылку
        // student1 — это "пульт управления" этим объектом
        Student student1 = new Student("Алексей", 19, "ИТ-101", 4.7);
        // ↑ переменная student1 хранит АДРЕС объекта в памяти

        // Шаг 2: создаём объект доцента
        Docent prof1 = new Docent("Мария Ивановна", 42, "Программирования", 12);
        // ↑ переменная prof1 хранит АДРЕС другого объекта

        // Шаг 3: работаем со студентом
        student1.showInfo();  // вызываем метод У ОБЪЕКТА, на который "смотрит" student1
        greet(student1);      // передаём ссылку на студента в метод greet

        // Шаг 4: работаем с доцентом
        prof1.showInfo();
        greet(prof1);

        // 🔚 Когда main() закончится — student1 и prof1 перестанут существовать.
        // Но объекты в памяти ещё могут жить, пока их не удалит сборщик мусора.
    }
}